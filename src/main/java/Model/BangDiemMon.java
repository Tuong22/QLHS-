package Model;

public class BangDiemMon {
	private String MaBangDiemMon;
	private String MaNH;
	private String MaLop;
	private String MaMH;
	private String MaHK;
	public BangDiemMon(String maBangDiemMon, String maNH, String maLop, String maMH, String maHK) {
		MaBangDiemMon = maBangDiemMon;
		MaNH = maNH;
		MaLop = maLop;
		MaMH = maMH;
		MaHK = maHK;
	}
	public String getMaBangDiemMon() {
		return MaBangDiemMon;
	}
	public void setMaBangDiemMon(String maBangDiemMon) {
		MaBangDiemMon = maBangDiemMon;
	}
	public String getMaNH() {
		return MaNH;
	}
	public void setMaNH(String maNH) {
		MaNH = maNH;
	}
	public String getMaLop() {
		return MaLop;
	}
	public void setMaLop(String maLop) {
		MaLop = maLop;
	}
	public String getMaMH() {
		return MaMH;
	}
	public void setMaMH(String maMH) {
		MaMH = maMH;
	}
	public String getMaHK() {
		return MaHK;
	}
	public void setMaHK(String maHK) {
		MaHK = maHK;
	}
	
	
}

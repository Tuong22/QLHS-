package Model;

public class TraCuuKhoi {
	private String tenKhoi;
	private int siSo;
	public TraCuuKhoi(String tenKhoi, int siSo) {
		super();
		this.tenKhoi = tenKhoi;
		this.siSo = siSo;
	}
	public String getTenKhoi() {
		return tenKhoi;
	}
	public void setTenKhoi(String tenKhoi) {
		this.tenKhoi = tenKhoi;
	}
	public int getSiSo() {
		return siSo;
	}
	public void setSiSo(int siSo) {
		this.siSo = siSo;
	}

	
}
